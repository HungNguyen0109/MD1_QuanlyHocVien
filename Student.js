class Student {
    constructor(id, name, age) {
        this.id = id
        this.name = name
        this.age = age
    }
    getID() {
        return this.id
    }
    setID(id) {
        this.id = id;
    }
    getName() {
        return this.name
    }
    setName(name) {
        this.name = name;
    }
    getAge() {
        return this.age
    }
    setAge(age) {
        this.age = age;
    }
}